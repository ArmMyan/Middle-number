list = [1, 2, 3, 4, 5, 6,7]

first_middle = int(len(list) / 2) - 1 # = 3 - 1 = 2
second_middle = int(len(list) / 2)    # = 3
last_middle = int(len(list) / 2) + 1 # = 3 + 1 = 4
print(list[first_middle])  # 3
print(list[second_middle]) # 4
print(list[last_middle]) # 5


